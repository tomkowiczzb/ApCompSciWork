//division example

import static java.lang.System.*;

public class Divide
{
   public static void main( String args[] )
   {
		out.println( 1 / 2  );
		out.println( 1 / 2.0 );
		out.println( 1.0 / 2  );
		out.println( 1.0 / 2.0 );	
			
		//out.println( 1 / 0  );	//division by zero
		out.println( 0 / 1 );							

		out.println(2 / 3);

		out.println(3 / 2);

		out.println(9 / 3);

		out.println(9.2 / 3);
   }
}